public class SeasonsDB {
    private int _idSesons;
    private int seasonNumber;
    private int seeSeason;
    private int _idSerials;

    public int get_idSesons() {
        return _idSesons;
    }

    public void set_idSesons(int _idSesons) {
        this._idSesons = _idSesons;
    }

    public int getSeasonNumber() {
        return seasonNumber;
    }

    public void setSeasonNumber(int seasonNumber) {
        this.seasonNumber = seasonNumber;
    }

    public int getSeeSeason() {
        return seeSeason;
    }

    public void setSeeSeason(int seeSeason) {
        this.seeSeason = seeSeason;
    }

    public int get_idSerials() {
        return _idSerials;
    }

    public void set_idSerials(int _idSerials) {
        this._idSerials = _idSerials;
    }
}
